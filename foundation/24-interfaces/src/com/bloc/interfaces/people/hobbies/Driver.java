/* Methods in an interface do not have a body
 * When a class implements them, that is where
 * they are defined
 */

public interface Driver {
	
	/*
	 * getInCar
	 *
	 * The Driver gets in their car
	 *
	 * @return nothing
	 */
	
	public void getInCar();

	/*
	 * startEngine
	 *
	 * The Driver starts the engine of their car
	 *
	 * @return nothing
	 */

	public void startEngine();
	
	/*
	 * driveFast
	 *
	 * Drive fast, Driver!
	 *
	 * @return nothing
	 */
	
	public void driveFast();
	
}